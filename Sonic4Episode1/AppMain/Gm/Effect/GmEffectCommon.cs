    private static void GmEfctCmnBuildDataInit()
    {
        gm_efct_cmn_proc_state = GME_EFCT_CMN_PROCESS_STATE_WAIT_START;
    }

    private static bool GmEfctCmnBuildDataLoopInitPart()
    {
        int num1 = 97 / StageCount;
        int num2 = num1 * stage;
        int num3 = num2 + num1;
        if (stage == StageCount - 1)
            num3 = 97;
        for (int ame_idx = num2; ame_idx < num3; ++ame_idx)
        {
            GMS_EFCT_CMN_CREATE_PARAM efctCmnCreateParam = gm_efct_cmn_create_param_tbl[ame_idx];
            int modelIdx = efctCmnCreateParam.create_param.model_idx;
            int modelDworkNo = gmEfctCmnGetModelDworkNo(ame_idx);
            int objectDworkNo = gmEfctCmnGetObjectDworkNo(ame_idx);
            int mdlAmbtexIdx = efctCmnCreateParam.mdl_ambtex_idx;
            int mdlAmbtexDworkNo = gmEfctCmnGetMdlAmbtexDworkNo(ame_idx);
            int mdlTexlistDworkNo = gmEfctCmnGetMdlTexlistDworkNo(ame_idx);
            if (modelIdx != -1)
            {
                ambtex_dwork = ObjDataGet(mdlAmbtexDworkNo);
                ObjDataLoadAmbIndex(ambtex_dwork, mdlAmbtexIdx, eff_cmn_arc);
                gm_efct_cmn_mdl_tex_reg_id_list[model_reg_cnt] = ObjAction3dESTextureLoadToDwork(ObjDataGet(mdlTexlistDworkNo), readAMBFile(ambtex_dwork.pData), ref texlistbuf);
                ObjDataLoadAmbIndex(ObjDataGet(modelDworkNo), modelIdx, eff_cmn_arc);
                gm_efct_cmn_model_reg_id_list[model_reg_cnt] = ObjAction3dESModelLoadToDwork(ObjDataGet(objectDworkNo), (AmbChunk)ObjDataGet(modelDworkNo).pData, 0U);
                ++model_reg_cnt;
            }
        }
        if (stage == StageCount - 1)
        {
            eff_cmn_arc = null;
            ambtex_dwork = null;
            texlistbuf = null;
            model_reg_cnt = 0;
            stage = 0;
            return true;
        }
        ++stage;
        return false;
    }

    private static void GmEfctCmnBuildDataLoopInit()
    {
        eff_cmn_arc = readAMBFile(ObjDataGetInc(ObjDataGet(5)));
        gm_efct_cmn_model_reg_num = 97;
        if (gm_efct_cmn_model_reg_num > 0)
        {
            gm_efct_cmn_model_reg_id_list = new int[gm_efct_cmn_model_reg_num];
            gm_efct_cmn_mdl_tex_reg_id_list = new int[gm_efct_cmn_model_reg_num];
            for (int index = 0; index < gm_efct_cmn_model_reg_num; ++index)
                gm_efct_cmn_model_reg_id_list[index] = gm_efct_cmn_mdl_tex_reg_id_list[index] = -1;
        }
        ambtex_dwork = ObjDataGet(17);
        ObjDataLoadAmbIndex(ambtex_dwork, 97, eff_cmn_arc);
        gm_efct_cmn_tex_reg_id = ObjAction3dESTextureLoadToDwork(ObjDataGet(18), readAMBFile(ambtex_dwork.pData), ref texlistbuf);
        GmEfctCmnBuildDataLoopInitPartWorking = true;
    }

    private static bool GmEfctCmnBuildDataLoop()
    {
        bool flag = true;
        switch (gm_efct_cmn_proc_state)
        {
            case GME_EFCT_CMN_PROCESS_STATE_NOP:
                return true;
            case GME_EFCT_CMN_PROCESS_STATE_WAIT_START:
                if (GsMainSysGetDisplayListRegistNum() < 192)
                {
                    if (!GmEfctCmnBuildDataLoopInitPartWorking)
                        GmEfctCmnBuildDataLoopInit();
                    else if (GmEfctCmnBuildDataLoopInitPart())
                    {
                        gm_efct_cmn_proc_state = GME_EFCT_CMN_PROCESS_STATE_WAIT_COMPLETE;
                        GmEfctCmnBuildDataLoopInitPartWorking = false;
                    }
                }
                return false;
            default:
                if (gm_efct_cmn_tex_reg_id != -1)
                {
                    if (amDrawIsRegistComplete(gm_efct_cmn_tex_reg_id))
                        gm_efct_cmn_tex_reg_id = -1;
                    else
                        flag = false;
                }
                for (int index = 0; index < gm_efct_cmn_model_reg_num; ++index)
                {
                    if (gm_efct_cmn_mdl_tex_reg_id_list[index] != -1)
                    {
                        if (amDrawIsRegistComplete(gm_efct_cmn_mdl_tex_reg_id_list[index]))
                            gm_efct_cmn_mdl_tex_reg_id_list[index] = -1;
                        else
                            flag = false;
                    }
                    if (gm_efct_cmn_model_reg_id_list[index] != -1)
                    {
                        if (amDrawIsRegistComplete(gm_efct_cmn_model_reg_id_list[index]))
                            gm_efct_cmn_model_reg_id_list[index] = -1;
                        else
                            flag = false;
                    }
                }
                if (flag)
                    gm_efct_cmn_proc_state = GME_EFCT_CMN_PROCESS_STATE_NOP;
                return flag;
        }
    }

    private static void GmEfctCmnFlushDataInit()
    {
        gm_efct_cmn_proc_state = GME_EFCT_CMN_PROCESS_STATE_WAIT_START;
    }

    private static void GmEfctCmnFlushDataLoopInit()
    {
        int index = 0;
        for (int ame_idx = 0; ame_idx < 97; ++ame_idx)
        {
            GMS_EFCT_CMN_CREATE_PARAM efctCmnCreateParam = gm_efct_cmn_create_param_tbl[ame_idx];
            int modelDworkNo = gmEfctCmnGetModelDworkNo(ame_idx);
            int objectDworkNo = gmEfctCmnGetObjectDworkNo(ame_idx);
            if (efctCmnCreateParam.create_param.model_idx != -1)
            {
                gm_efct_cmn_model_reg_id_list[index] = ObjAction3dESModelReleaseDwork(ObjDataGet(objectDworkNo));
                ObjDataRelease(ObjDataGet(modelDworkNo));
                gm_efct_cmn_mdl_tex_reg_id_list[index] = ObjAction3dESTextureReleaseDwork(ObjDataGet(gmEfctCmnGetMdlTexlistDworkNo(ame_idx)));
                ObjDataRelease(ObjDataGet(gmEfctCmnGetMdlAmbtexDworkNo(ame_idx)));
                ++index;
            }
        }
        gm_efct_cmn_tex_reg_id = ObjAction3dESTextureReleaseDwork(ObjDataGet(18));
        ObjDataRelease(ObjDataGet(17));
        ObjDataRelease(ObjDataGet(5));
    }

    private static bool GmEfctCmnFlushDataLoop()
    {
        bool flag = true;
        int index = 0;
        switch (gm_efct_cmn_proc_state)
        {
            case 0:
                return true;
            case 1:
                if (GsMainSysGetDisplayListRegistNum() < 240)
                {
                    GmEfctCmnFlushDataLoopInit();
                    gm_efct_cmn_proc_state = GME_EFCT_CMN_PROCESS_STATE_WAIT_COMPLETE;
                }
                return false;
            default:
                if (gm_efct_cmn_model_reg_num != 0)
                {
                    for (int ame_idx = 0; ame_idx < 97; ++ame_idx)
                    {
                        if (gm_efct_cmn_create_param_tbl[ame_idx].create_param.model_idx != -1)
                        {
                            if (gm_efct_cmn_model_reg_id_list[index] != -1)
                            {
                                if (ObjAction3dESModelReleaseDworkCheck(ObjDataGet(gmEfctCmnGetObjectDworkNo(ame_idx)), gm_efct_cmn_model_reg_id_list[index]))
                                    gm_efct_cmn_model_reg_id_list[index] = -1;
                                else
                                    flag = false;
                            }
                            if (gm_efct_cmn_mdl_tex_reg_id_list[index] != -1)
                            {
                                if (ObjAction3dESTextureReleaseDworkCheck(ObjDataGet(gmEfctCmnGetMdlTexlistDworkNo(ame_idx)), gm_efct_cmn_mdl_tex_reg_id_list[index]))
                                    gm_efct_cmn_mdl_tex_reg_id_list[index] = -1;
                                else
                                    flag = false;
                            }
                            ++index;
                        }
                    }
                }
                if (gm_efct_cmn_tex_reg_id != -1)
                {
                    if (ObjAction3dESTextureReleaseDworkCheck(ObjDataGet(18), gm_efct_cmn_tex_reg_id))
                        gm_efct_cmn_tex_reg_id = -1;
                    else
                        flag = false;
                }
                if (flag)
                {
                    if (gm_efct_cmn_mdl_tex_reg_id_list != null)
                        gm_efct_cmn_mdl_tex_reg_id_list = null;
                    if (gm_efct_cmn_model_reg_id_list != null)
                    {
                        gm_efct_cmn_model_reg_id_list = null;
                        gm_efct_cmn_model_reg_num = 0;
                    }
                    gm_efct_cmn_proc_state = GME_EFCT_CMN_PROCESS_STATE_NOP;
                }
                return flag;
        }
    }

    private static GMS_EFFECT_3DES_WORK GmEfctCmnEsCreate(
      OBS_OBJECT_WORK parent_obj,
      int efct_cmn_idx)
    {
        GMS_EFCT_CMN_CREATE_PARAM efctCmnCreateParam = gm_efct_cmn_create_param_tbl[efct_cmn_idx];
        OBS_DATA_WORK model_dwork;
        OBS_DATA_WORK object_dwork;
        OBS_DATA_WORK ambtex_dwork;
        OBS_DATA_WORK texlist_dwork;
        if (efctCmnCreateParam.create_param.model_idx != -1)
        {
            int ameIdx = efctCmnCreateParam.create_param.ame_idx;
            model_dwork = ObjDataGet(gmEfctCmnGetModelDworkNo(ameIdx));
            object_dwork = ObjDataGet(gmEfctCmnGetObjectDworkNo(ameIdx));
            ambtex_dwork = ObjDataGet(gmEfctCmnGetMdlAmbtexDworkNo(ameIdx));
            texlist_dwork = ObjDataGet(gmEfctCmnGetMdlTexlistDworkNo(ameIdx));
        }
        else
        {
            model_dwork = null;
            object_dwork = null;
            ambtex_dwork = ObjDataGet(17);
            texlist_dwork = ObjDataGet(18);
        }
        return GmEffect3dESCreateByParam(efctCmnCreateParam.create_param, parent_obj, ObjDataGet(5).pData, ObjDataGet(gmEfctCmnGetAmeDworkNo(efctCmnCreateParam.create_param.ame_idx)), ambtex_dwork, texlist_dwork, model_dwork, object_dwork);
    }

