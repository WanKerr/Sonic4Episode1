    private static void GmEfctCmnUpdateInvincibleSubPart(
      GMS_EFFECT_3DES_WORK efct_3des,
      OBS_OBJECT_WORK ply_obj)
    {
        OBS_OBJECT_WORK objWork = efct_3des.efct_com.obj_work;
        objWork.dir.z += GMD_EFCT_CMN_INVINCIBLE_SUB_ANGLE_RATE;
        objWork.spd.x = FX_Mul(ply_obj.pos.x - objWork.pos.x, GMD_EFCT_CMN_INVINCIBLE_SUB_DIST_TO_SPD_FACTOR);
        objWork.spd.y = FX_Mul(ply_obj.pos.y - objWork.pos.y, GMD_EFCT_CMN_INVINCIBLE_SUB_DIST_TO_SPD_FACTOR);
        if (objWork.spd.x > 0 && ply_obj.move.x > 0 || objWork.spd.x < 0 && ply_obj.move.x < 0)
            objWork.spd.x += FX_Mul(ply_obj.move.x, GMD_EFCT_CMN_INVINCIBLE_SUB_PLYSPD_TO_SPD_FACTOR);
        if ((objWork.spd.y <= 0 || ply_obj.move.y <= 0) && (objWork.spd.y >= 0 || ply_obj.move.y >= 0))
            return;
        objWork.spd.y += FX_Mul(ply_obj.move.y, GMD_EFCT_CMN_INVINCIBLE_SUB_PLYSPD_TO_SPD_FACTOR);
    }

